<h2 class="tamtex20">Qu’est ce que l’hydroponie?</h2><br>

<p>Hydro-quoi ??</p>

<p>Nous rencontrons beaucoup de réactions différentes lorsque nous racontons que nous cultivons une serre hydroponique. La plupart ne connaissent pas du tout, certains en ont une bien mauvaise image, d’autres pensent que nous faisons pousser des plantes interdites…!
Pourtant l’hydroponie ne date pas d’hier, elle était utilisée par les plus grandes civilisations, Incas, Aztèques, les célèbres jardins suspendus de Babylone… car elle présente de nombreux atouts.</p>

<p>Le principe est simple, il s’agit de faire pousser des plantes en remplaçant la terre par un substrat inerte, ou, comme dans notre cas de l’eau. A celle ci nous ajouterons des sels minéraux et de l’oxygène. Les racines flottent avec bonheur dans cette eau oú elles peuvent se nourrir avec facilité des ions que nous mettons à leur disposition. Le tout fonctionne en circuit fermé alimenté par une pompe.</p>

<p>Les avantages sont très nombreux par exemple:</p>

<p class="margen20">1- Grace au circuit fermé, nous économisons jusqu'à 95% d’eau par rapport à de l’agriculture en terre.</p>

<p class="margen20">2- L’accès à l’eau rend le travail du maraicher plus exigeant, nous contrôlons en permanence que nos racines aient de quoi manger, en bonne quantité, que l’oxygène soit suffisant, que le PH soit idéal pour la bonne absorption des nutriments. Nous donnons à la plante exactement ce dont elle a besoin pour son bon développement, ni plus ni moins.
</p>

<p class="margen20">3- Ce dernier point rend nos plantes absolument délicieuses, si vous êtes septiques venez faire un tour dans notre serre, c’est avec plaisir que nous vous ferons déguster nos produits et changer les idées erronées que l’hydroponie donne des légumes sans saveur.</p>

<p class="margen20">4- Protection du sol : la totalité de la solution nutritive apportée à la plante est absorbée, rien n’est perdu dans le sol, il n’y a donc aucun risque de pollution des nappes phréatiques.</p>

<p class="margen20">5- Nous n’utilisons pas d’herbicide, c’est évident ! Pas de terre, pas de mauvaises herbes !</p> 

<p class="margen20">6- Dans le cadre de notre projet nous n’utilisons aucun pesticide, mais c’est assez facile car nos plantes ont une croissance rapide et saine et résistent donc plus facilement aux attaques de nuisibles.</p>

<p class="margen20">7- L’hydroponie permet de contrôler plusieurs facteurs qui sont impossibles à contrôler en terre, sans carence, la plante peut exprimer tout son potentiel génétique. C’est d’ailleurs une grande révélation pour les plantes médicinales car les principes actifs des plantes ayant poussé en hydroponie sont beaucoup plus concentrés que sur la même plante ayant poussé en terre.</p>

<p>L’hydroponie, lorsqu’elle est bien utilisée, change complétement la manière dont nous voyons pousser les plantes, c’est une expérience qu’il faut essayer, voir et surtout goûter.</p><br><br>

<h2 class="tamtex20">Est ce que c’est Bio ?</h2><br>

<p>Nous pratiquons ce que nous appelons l’agriculture raisonnée. Nous nous imposons les mêmes défis que l’agriculture biologique, mais le label Bio ne s’applique qu’aux cultures en terre.<br>Nous nous engageons à utiliser des méthodes de culture durables et responsables, nous n’utilisons pas de pesticide ni d’herbicide. Nous privilégions la lutte biologique pour défendre nos plantes contre les attaques d’insectes ravageurs. L’eau que nous utilisons est récupérée et recyclée, nos déchets verts sont compostés, nous livrons nos produits le jour même de leur récolte et sur commande pour éviter le gaspillage. Nous privilégions des races de plantes en nous basant sur leur goût et non sur leur résistance au transport. Pour contrer une importation peu écologique des espèces importées depuis l’Asie qui arrivent donc fatiguées du transport, nous offrons aux restaurateurs les mêmes plantes, fraiches que nous produisons localement.<br>Offrir un aliment savoureux et sain est notre priorité.</p>

